import UIKit

extension UIColor {
    static func colorEpicBlack() -> UIColor {
        return UIColor(red: 0.01176470588, green: 0.01176470588, blue: 0.01176470588, alpha: 1)
    }
    
    static func colorEpicGreen() -> UIColor {
        return UIColor(red: 0, green: 0.8509803921568627, blue: 0.564705882352941, alpha: 1)
    }
    
    static func colorEpicWhite() -> UIColor {
        return UIColor.whiteColor()
    }
    
    static func colorBlendedEpicBlack() -> UIColor {
        return UIColor(red: 0.01176470588, green: 0.01176470588, blue: 0.01176470588, alpha: 0.2)
    }
    
}